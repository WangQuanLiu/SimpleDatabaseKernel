	inline int  letter_convert_to_index(const char alphabet[], size_t size, char ch)
	{
		int index = 0;
		unsigned i;
		for (i = 0; i < size; i++) {
			if (ch == alphabet[i])
			{
				index = i;
				break;
			}
		}
		return index;
	}
